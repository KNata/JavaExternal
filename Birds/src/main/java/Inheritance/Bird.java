package Inheritance;

public class Bird {

    Bird() {
        System.out.println("New bird was created...");
    }

    public void fly() {
        System.out.println("Bird flies...");
    }

    public void sing() {
        System.out.println("Bird sing...");
    }

    public void madeEggs() {
        System.out.println("1 egg...");
    }
}